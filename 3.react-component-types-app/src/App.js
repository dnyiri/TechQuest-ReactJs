import React, { Component } from 'react';

// Stateless functional component
const Header = (props) => {
  return (
    <header>
      <h1>{props.title}</h1>
      <nav>
        <ul>
          <li><a href="#">Home</a></li>
          <li><a href="#">About</a></li>
          <li><a href="#">Contact</a></li>
        </ul>
      </nav>
    </header>
  );
}

// Stateful class component
class Counter extends Component {
  constructor(props) {
    super(props);
    this.state = {
      count: 0
    };
  }

  incrementCount = () => {
    this.setState({
      count: this.state.count + 1
    });
  }

  render() {
    return (
      <div>
        <p>Count: {this.state.count}</p>
        <button onClick={this.incrementCount}>Increment</button>
      </div>
    );
  }
}

// Parent component that renders both Header and Counter components
class App extends Component {
  render() {
    return (
      <div className="App">
        <Header title="My App" />
        <Counter />
      </div>
    );
  }
}

export default App;
